                <h7>4 - Way Test</h7>
            </div>
            <div style="padding:10px;">
            <div>
              <p>From the earliest days of the organization,   Rotarians were concerned with promoting high ethical standards in their   professional lives. One of the world's most widely printed and quoted   statements of business ethics is The 4-Way Test, which was created in   1932 by Rotarian Herbert J. Taylor (who later served as RI president)   when he was asked to take charge of a company that was facing   bankruptcy. This 24-word test for employees to follow in their business   and professional lives became the guide for sales, production,   advertising, and all relations with dealers and customers, and the   survival of the company is credited to this simple philosophy. Adopted   by Rotary in 1943, The 4-Way Test has been translated into more than a   hundred languages and published in thousands of ways. It asks the   following four questions:</p>
              <p> &quot;Of the things we think, say or do:</p>
              <p> Is it the TRUTH?<br />
                Is it FAIR to all concerned?<br />
                Will it build GOODWILL and BETTER FRIENDSHIPS?<br />
                Will it be BENEFICIAL to all concerned?&quot;</p>
